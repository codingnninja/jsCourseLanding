let currentQuestionIndex = 0;
const lastImageIndex = Questions.length - 1;

const display = (currentQuestionIndex) => {
    //display slide image
    const currentQuestion = Questions[currentQuestionIndex];
    const sliderImage = document.getElementById('slider_image');
    sliderImage.src = currentQuestion.image;

    makeOption(currentQuestion, currentQuestionIndex);
   //create slide number
    makeSlideNumber(currentQuestionIndex, lastImageIndex);
}

function makeSlideNumber (currentQuestionIndex, lastImageIndex) {
    const slideNumber = `${currentQuestionIndex + 1} / ${lastImageIndex + 1}`;

    const slideNumberUI = document.querySelector('.slide-number');
    slideNumberUI.innerHTML = slideNumber;
}

const makeOption = (question, currentQuestionIndex) => {
    const options = [];
    const quiz = document.querySelector("#quiz")
    console.log(quiz)
    for(const letter in question.options) {
        options.push(
            `<li class="option">
                <label id = "${letter}">
                    <input type="radio" name="question${currentQuestionIndex}" id="${letter}" onclick="markQuestion(event)">
                    <span id="${letter}" style="margin-left:10px"> 
                        ${question.options[letter]}
                    </span>
                </label>
            </li>`
        );
    }
    quiz.innerHTML = `
                    <div class="question">${question.question}</div>
                      <ul class="options">
                        ${options.join(" ")}
                    </ul>`
}

display(currentQuestionIndex);// Display the first image in the array

const markQuestion = (event) => {

    const userAnswer = event.target.id;
    const answer = Questions[currentQuestionIndex].answer;
    
    if( userAnswer != answer ) {
        notify("Wrong answer", false);
        return true;
    }
    notify("Correct answer", true)
}

const notify = (message, status) => {
    const nextQuestionIndex = currentQuestionIndex + 1;
    const quiz = document.querySelector("#quiz")
    const icon = status ? "checkmark.png" : "cancel.png";
    const button = status ? next(nextQuestionIndex) : watchTutorial(nextQuestionIndex);
    const messageView = `
    <div class="icon-container zoom-in-out-box">
        <img src="https://res.cloudinary.com/nyscapp/image/upload/v1662907001/viewtern/${icon}">
    </div>
    <div class="error-message">${message}</div>
    ${button}`; 
    quiz.innerHTML = messageView;
}

const next = (index) => {
    currentQuestionIndex = index;
    if(currentQuestionIndex === Questions.length){
        return `
        <a href="https://youtoocancode.com/html-css"> Learn HTML & CSS</a>
        `
    }
    return `<button onclick="display(${index})"> Next </button>
    `
}

const watchTutorial = (index) => {
    const correctionCordinate = {
        x:''
    }
    
    makeCorrection(Questions[currentQuestionIndex]);
    return `${next(index)}`;
}

const makeCorrection = (question) => {
    const sliderImage = document.getElementById('slider_image');
    sliderImage.src = question.correctionImage;
}

const resolveElement = (obj) => {
    if(obj.nodeType === 1) {
        return obj;
    } else if (obj.target) {
        return obj.target;
    }
    throw("element and events are expected");
}

const hide = (element) => {
    const tag = resolveElement(element);
    tag.classList.add('hide');
}

const show = (element) => {
    const tag = resolveElement(element);
    tag.classList.remove('hide');
}

const showGame = () => {
    const btn = document.querySelector('.btn');
    btn.addEventListener('click', (event) => {
        const landing = document.querySelector('.landing');
        const game = document.querySelector('.game');
        hide(landing);
        show(game)
    })
}
